#! /bin/bash

IOCTL2="/root/driver/driver/universeII-Rosewood-v1.5.0/tools/vme-wl"


# 1. software reset
$IOCTL2 0x1000EF24 0x1
$IOCTL2 0x3000EF24 0x1

# 2. board config 
$IOCTL2 0x10008000 0x110
$IOCTL2 0x30008000 0x110

# 3. sampling frequency 
$IOCTL2 0x100080D8 0x0
$IOCTL2 0x300080D8 0x0

# 4. all group enabled
$IOCTL2 0x10008120 0xF
$IOCTL2 0x30008120 0xF

# 5. 1024 samples (full window)
$IOCTL2 0x10008020 0x0
$IOCTL2 0x30008020 0x0

# 6. post-trigger
$IOCTL2 0x10001014 0x10
$IOCTL2 0x10001114 0x10
$IOCTL2 0x10001214 0x10
$IOCTL2 0x10001314 0x10
$IOCTL2 0x30001014 0x10
$IOCTL2 0x30001114 0x10
$IOCTL2 0x30001214 0x10
$IOCTL2 0x30001314 0x10
$IOCTL2 0x10008114 0x10
$IOCTL2 0x30008114 0x10

# 7. DC offset and correction level 
$IOCTL2 0x10001098 0xF6666
$IOCTL2 0x10001198 0xF6666
$IOCTL2 0x10001298 0xF6666
$IOCTL2 0x10001398 0xF6666
$IOCTL2 0x30001098 0xF6666
$IOCTL2 0x30001198 0xF6666
$IOCTL2 0x30001298 0xF6666
$IOCTL2 0x30001398 0xF6666
$IOCTL2 0x10001020 0x3
$IOCTL2 0x10001120 0x3
$IOCTL2 0x10001220 0x3
$IOCTL2 0x10001320 0x3
$IOCTL2 0x30001020 0x3
$IOCTL2 0x30001120 0x3
$IOCTL2 0x30001220 0x3
$IOCTL2 0x30001320 0x3


# 8. threshold
$IOCTL2 0x10001080 0xF000
$IOCTL2 0x10001180 0xF000
$IOCTL2 0x10001280 0xF000
$IOCTL2 0x10001380 0xF000
$IOCTL2 0x30001080 0xF000
$IOCTL2 0x30001180 0xF000
$IOCTL2 0x30001280 0xF000
$IOCTL2 0x30001380 0xF000

# 9. bus extention mode
$IOCTL2 0x1000EF00 0x120
$IOCTL2 0x3000EF00 0x120

# 9. software clear 
$IOCTL2 0x1000EF28 0x1
$IOCTL2 0x3000EF28 0x1


# 9. run start
#$IOCTL2 0x10008100 0x4
#$IOCTL2 0x30008100 0x4



